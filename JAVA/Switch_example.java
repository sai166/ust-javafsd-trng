package com.ust;

public class Switch_example {
public static void main(String args[]) {
	String str="Green1";
	switch(str) {
	case "Red":
		System.out.println("im in red");
		System.out.println("*");
	default:
		System.out.println("default");
		break;
	case "Green":
		System.out.println("im in green");
		System.out.println("*");
		break;
	case "Yellow":
		System.out.println("im in yellow");
		System.out.println("*");
		break;
	
	
		
		
		
	}
	
}
}
