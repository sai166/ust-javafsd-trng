package com.ust;

public class Switch_ex1 {
	public static void main() {
		int expression=2;
		//switch statement to check size
		switch(expression) {
		case 1:
			System.out.println("case1");
			//matching class
		case 2:
			System.out.println("case2");
		case 3:
			System.out.println("case3");
			
			default:
				System.out.println("Default case");
			
		}
	}

}
